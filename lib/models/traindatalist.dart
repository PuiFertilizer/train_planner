  String trainType; //ประเภทขบวน
  String departureStation; //สถานีต้นทาง
  String departureTime;
  String arriveStation; //สถานีปลายทาง
  String arriveTime;
  String line; //สาย
  List<Seating> seats; //รายระเอียดที่นั่ง
  List<Destination> desinations; //ทิวทัศน์ข้างทาง
