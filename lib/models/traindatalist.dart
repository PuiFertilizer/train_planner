  TrainList(
      {required this.trainNo,
      required this.trainType,
      required this.departureStation,
      required this.arriveTime,
      required this.arriveStation,
      required this.departureTime,
      required this.line,
      required this.seats,
      required this.desinations,
      required this.chart});
}

